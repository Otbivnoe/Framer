#ifndef Macroses_h
#define Macroses_h

#define NUI_WIDTH(VIEW) CGRectGetWidth(VIEW.bounds)
#define NUI_HEIGHT(VIEW) CGRectGetHeight(VIEW.bounds)

#define NUI_MID_X(VIEW) CGRectGetMidX(VIEW.frame)
#define NUI_MAX_X(VIEW) CGRectGetMaxX(VIEW.frame)
#define NUI_MIN_X(VIEW) CGRectGetMinX(VIEW.frame)

#define NUI_MID_Y(VIEW) CGRectGetMidY(VIEW.frame)
#define NUI_MAX_Y(VIEW) CGRectGetMaxY(VIEW.frame)
#define NUI_MIN_Y(VIEW) CGRectGetMinY(VIEW.frame)

#endif /* Macroses_h */
